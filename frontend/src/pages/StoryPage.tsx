const Buttons = styled.div`
  display: flex;
  gap: 12px;
  position: absolute;
  bottom: 40px;
  left: 20px;
  width: calc(100% - 40px);
`;

export default function StoryPage() {
  const navigate = useNavigate();
  const location = useLocation();
  const title = location.state?.title || "Untitled Story";
  const story = location.state?.story || "No story available";
  const storyId = location.state?.id;
  const [fontSize, setFontSize] = useState(16);
  const [isDeleting, setIsDeleting] = useState(false);
  const setToken = useClerkApiToken();

  // ÌÜ†ÌÅ∞ ÏÑ§Ï†ï
  useEffect(() => {
    const initializeToken = async () => {
      try {
        await setToken();
      } catch (error) {
        console.error("Failed to set token:", error);
      }
    };
    initializeToken();
  }, [setToken]);

  const goToHome = () => {
    navigate("/");
  };

  const createAnother = () => {
    navigate("/create");
  };

  const handleDelete = async () => {
    if (!storyId) return;

    if (window.confirm("Are you sure you want to delete this story?")) {
      setIsDeleting(true);
      try {
        // ÌÜ†ÌÅ∞ÏùÑ Îã§Ïãú ÏÑ§Ï†ïÌïòÏó¨ ÏµúÏã† ÏÉÅÌÉúÎ°ú ÏóÖÎç∞Ïù¥Ìä∏
        await setToken();
        await deleteStory(storyId);
        navigate("/", { replace: true });
      } catch (error) {
        console.error("Error deleting story:", error);
        alert("Error deleting story. Please try again.");
      } finally {
        setIsDeleting(false);
      }
    }
  };

  const increaseFontSize = () => {
    setFontSize((prev) => Math.min(prev + 2, 22));
  };

  const decreaseFontSize = () => {
    setFontSize((prev) => Math.max(prev - 2, 12));
  };

  // Ïä§ÌÜ†Î¶¨ ÌÖçÏä§Ìä∏Î•º Î¨∏Îã®ÏúºÎ°ú ÎÇòÎàÑÎäî Ìï®Ïàò
  const formatStory = (storyText: string) => {
    // Ï§ÑÎ∞îÍøàÏùÑ Í∏∞Ï§ÄÏúºÎ°ú ÎÇòÎàÑÍ≥† Îπà Ï§ÑÏùÄ Ï†úÍ±∞
    const lines = storyText
      .split("\n")
      .map((line) => line.trim())
      .filter((line) => line.length > 0);

    let firstStoryParagraph = -1; // Ï≤´ Î≤àÏß∏ Ïä§ÌÜ†Î¶¨ Î¨∏Îã® Ïù∏Îç±Ïä§

    return lines.map((line, index) => {
      // ÎßàÌÅ¨Îã§Ïö¥ Ìó§Îçî Ï≤òÎ¶¨ (## Ï†úÍ±∞ÌïòÍ≥† h2Î°ú Î≥ÄÌôò)
      if (line.startsWith("##")) {
        const headerText = line.replace(/^#+\s*/, "");
        return <h2 key={index}>{headerText}</h2>;
      }

      // "The End" Ï≤òÎ¶¨
      if (
        line.includes("The End") ||
        line.includes("üåô") ||
        line.includes("‚ú®")
      ) {
        return (
          <p key={index} className="story-ending">
            {line}
          </p>
        );
      }

      // Ï≤´ Î≤àÏß∏ Ïä§ÌÜ†Î¶¨ Î¨∏Îã® Ï∞æÍ∏∞ (Ìó§ÎçîÍ∞Ä ÏïÑÎãå Ï≤´ Î≤àÏß∏ Î¨∏Îã®)
      if (firstStoryParagraph === -1) {
        firstStoryParagraph = index;
        return (
          <p key={index} className="story-beginning">
            {line}
          </p>
        );
      }

      // ÏùºÎ∞ò Î¨∏Îã® Ï≤òÎ¶¨
      return <p key={index}>{line}</p>;
    });
  };

  return (
    <StoryPageContainer>
      <Card>
        {storyId && (
          <TopBar>
            <Button
              $secondary
              style={{
                fontSize: 12,
                padding: "6px 12px",
                minHeight: "32px",
                width: "auto",
                margin: 0,
                borderRadius: "8px",
              }}
              onClick={handleDelete}
              disabled={isDeleting}
            >
              {isDeleting ? "Deleting..." : "Delete"}
            </Button>
          </TopBar>
        )}
        <CompactHeader>
          <h1>{title}</h1>
          <FontSizeControls>
            <button onClick={decreaseFontSize}>A-</button>
            <span>Font Size</span>
            <button onClick={increaseFontSize}>A+</button>
          </FontSizeControls>
        </CompactHeader>
        <StoryContainer>
          <StoryText $fontSize={fontSize}>{formatStory(story)}</StoryText>
        </StoryContainer>
        <Buttons>
          <Button $secondary onClick={goToHome} style={{ flex: 1 }}>
            Back to Stories
          </Button>
          <Button $primary onClick={createAnother} style={{ flex: 1 }}>
            Create Another
          </Button>
        </Buttons>
      </Card>
    </StoryPageContainer>
  );
}
